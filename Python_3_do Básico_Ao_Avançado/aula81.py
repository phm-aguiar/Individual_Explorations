# lambda

# lambda é uma função anônima
# lambda é uma função que pode ter vários argumentos mas apenas uma expressão
# lambda é uma função que retorna o valor da expressão

# lambda argumentos: expressão

# exemplo de lambda

# def soma(a, b):
#     return a + b

soma = lambda a, b: a + b

print(soma(1, 2))
print(soma(3, 4))
