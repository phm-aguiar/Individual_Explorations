#include "operatings.hpp"

int operatings::soma(int num, int num2)
{
	return (num + num2);
}

int operatings::sub(int num, int num2)
{
	return (num - num2);
}
int operatings::mult(int num, int num2)
{
	return (num * num2);
}

float operatings::divi(float num, float num2)
{
	return (num / num2);
}